package main
import "fmt"

func main() {
   var a int = 23
   var b int = 7

   fmt.Println("A+B=", a + b)
   fmt.Println("A-B=", a - b)
   fmt.Println("A*B=", a * b)
   fmt.Println("A/B=", a / b)// divisao =3 porque vai buscar a parte inteira
   fmt.Println("A%B=", a % b) //modulo da divisao =2
//para dar numero com virgula pode mudar para float64 para os dois



}